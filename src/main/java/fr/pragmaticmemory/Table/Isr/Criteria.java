package fr.pragmaticmemory.Table.Isr;

public class Criteria {

    private int id;
    private String name;


    public Criteria(int id, String name) {
        this.id = id;
        this.name = name;
    }


    public int getId() {
        return id;
    }


    public String getName() {
        return name;
    }
}
