package fr.pragmaticmemory.Table.Isr.inputData;

public class Broker {

    private int id;
    private String name;


    public Broker(int id, String name) {
        this.id = id;
        this.name = name;
    }


    public int getId() {
        return id;
    }


    public String getName() {
        return name;
    }
}
